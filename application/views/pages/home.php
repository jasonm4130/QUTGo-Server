	<h2>What is QUTgo</h2>
    <p>QUTgo is an app developed by Venom Tech as a final year IT Capstone Project, carrying the torch from previous development teams.
      It helps students and staff studying at the QUT Gardens Point campus to live a healthier lifestyle. The app achieves this through the use of challenges.</p>
    <p>Whenever a user leaves a building on campus, the app will issue a challenge to the user to walk to another building. This can also be initiated by the user themselves.
      Furthermore, users can add friends to issue walking challenges to others, or be challenged to walk more throughout the day by those people.</p>
    <p>By encouraging participants to walk that little extra they can accumliate more steps throughout their day.
      Sign up cannot be more simple - QUTgo utilises Google Services, meaning users need only sign into the app using their Google account.</p>

